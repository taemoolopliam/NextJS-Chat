import Head from "next/head";
import React, { useState, useEffect } from "react";
import { HubConnectionBuilder } from "@microsoft/signalr";

export default function Home() {
  const [name, setName] = useState("Tae");
  const [message, setMessage] = useState("");
  const [chat, setChat] = useState([]);
  const [hubCx, setHubCx] = useState(null);

  async function connectHub() {
    const newConnection = new HubConnectionBuilder()
      .withUrl("https://localhost:7175/ChatHub", {
        // accessTokenFactory: () => this.props.accessToken,
      })
      .withAutomaticReconnect()
      .build();
    await newConnection.start();

    newConnection.on("ReceiveMessage", (name, mess) => {
      setChat((c) => [`${name}: ${mess}`, ...c]);
    });
    setHubCx(newConnection);
  }

  useEffect(() => {
    connectHub();
  }, []);

  const handleSubmit = (event) => {
    event.preventDefault();
    hubCx.invoke("SendMessage", name, message);
    setMessage("");
  };

  const renderMessages = () => {
    return chat.map((value, index) => <div key={`${index++}`}>{value}</div>);
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="px-44 py-6">
        <h1>Chat App</h1>
        <div>
          <h2>Send Message</h2>

          <form onSubmit={handleSubmit}>
            <input type="text" value={name} onChange={(e) => setName(e.target.value)} placeholder="Name" />
            &nbsp;
            <input type="text" value={message} onChange={(e) => setMessage(e.target.value)} placeholder="Message" />
            &nbsp;
            <input type="submit" value="Submit" />
          </form>
        </div>
        <hr />
        <h2>Chat Log</h2>
        <div>{renderMessages()}</div>
      </div>
    </>
  );
}
